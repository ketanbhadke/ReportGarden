package com.reportgarden.finalproject.dto;

public class ContentRequest {

    public String getOwner() {
        return owner;
    }

    public void setOwner(String owner) {
        this.owner = owner;
    }

    public String getRepo() {
        return repo;
    }

    public void setRepo(String repo) {
        this.repo = repo;
    }

    private String owner;
    private String repo;

}
